#include<string.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)

{
	unsigned int	i;
	int				b;

	i = 0;
	b = 0;
	while ((s1[i] || s2[i]) && b == 0 && i < n)
	{
		if (s1[i] != s2[i])
			b = s1[i] - s2[i];
		i++;
	}
	return (b);
}
