char	*ft_strupcase(char *str)

{
	int	f;

	f = 0;
	while (str[f] != '\0')
	{
		if (f >= 97 && f <= 122)
			str[f] -= 32; // str[f] = str[f] - 32;
		f++;
	}
	return (*str);
}
