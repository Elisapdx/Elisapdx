int	ft_str_is_printable(char *str)

{
	int	e;

	e = 0;
	if (e == '\0')
		return (1);
	while (str[e] != '\0')
	{
		if (!((e >= 0 && e <= 31) && e == 127))
			return (0);
		e++;
	}
	return (1);
}
