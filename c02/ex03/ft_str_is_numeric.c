#include<string.h>

int	ft_str_is_numeric(char *str)

{
	int	b;

	b = 0;
	if (b == '\0')
		return (1);
	while (str[b] != '\0')
	{
		if (!(b >= 48 && b <= 57))
			return (0);
		b++;
	}
	return (1);
}
