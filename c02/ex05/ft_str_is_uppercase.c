#include<string.h>

int	ft_str_is_uppercase(char *str)

{
	int d;

	d = 0;
	if (d == '\0')
		return (1);
	while (str[d] != '\0')
	{
		if (!(d >= 65 && d <= 90))
			return (0);
		d++;
	}
	return (1);
}
