#include<string.h>

int	ft_str_is_lowercase(char *str)

{
	int	c;

	c = 0;
	if (c == '\0')
		return (1);
	while (str[c] != '\0')
	{
		if (!(c >= 97 && c <= 122))
			return (0);
		c++;
	}
	return (1);
}
