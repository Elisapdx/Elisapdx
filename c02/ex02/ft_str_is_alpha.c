#include<string.h>

int	ft_str_is_alpha(char *str)

{
	int	a;

	a = 0;
	if (a == '\0')
		return (1);
	while (str[a] != '\0')
	{
		if (!((a >= 65 && a <= 90) && (a >= 97 && a <= 122)))
			return (0);
		a++;
	}
	return (1);
}
